    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        String nombre= txtNombre.getText();
        String apellido= txtApellido.getText();
        String notaUno= txtNota1.getText();
        String notaDos= txtNota2.getText();
        String notaTres= txtNota3.getText();
        double nota1= Double.parseDouble(notaUno);
        double nota2= Double.parseDouble(notaDos);
        double nota3= Double.parseDouble(notaTres);
        double prom = (nota1+nota2+nota3)/3; 
        String promedio= String.valueOf(prom);
       if(!nombre.equals("")&& !apellido.equals("")&& !notaUno.equals("") && !notaDos.equals("") && !notaTres.equals("")){     
        int confirmacion =JOptionPane.showConfirmDialog(rootPane,"Esta Seguro?", "Confirmacion",JOptionPane.YES_NO_OPTION);
        if(confirmacion==0){
        ListaEstudiantes e = new ListaEstudiantes(nombre,apellido, nota1, nota2, nota3,prom);
        estudiantes.add(e);
        
        lblMensaje.setText("Se ha a√±adido al estudiante: "+nombre+" "+apellido);
        txtNombre.setText("");
        txtApellido.setText("");
        txtNota1.setText("");
        txtNota2.setText("");
        txtNota3.setText("");
        
        }else {
         lblMensaje.setText(" ");
        }
        }else{
        JOptionPane.showMessageDialog(rootPane, "DEBE INGRESAR TODOS LOS CAMPOS");
        }
        
       
        
    }//GEN-LAST:event_btnIngresarActionPerformed

